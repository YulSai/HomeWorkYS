package class_work_1;

public class Student {
    private int index;
    private String name;
    private int age;
    private float mark;
    private boolean olympiads;

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public float getMark() {
        return mark;
    }

    public void setMark(float mark) {
        this.mark = mark;
    }

    public boolean isOlympiads() {
        return olympiads;
    }

    public void setOlympiads(boolean olympiads) {
        this.olympiads = olympiads;
    }
}
